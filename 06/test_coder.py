from coder import *

def test_dest() -> None:
    assert dest("") == "000"
    assert dest("M") == "001"
    assert dest("D") == "010"
    assert dest("DM") == "011"
    assert dest("A") == "100"
    assert dest("AM") == "101"
    assert dest("AD") == "110"
    assert dest("ADM") == "111"

def test_comp() -> None:
    assert comp("0") == "0101010"
    assert comp("1") == "0111111"
    assert comp("-1") == "0111010"
    assert comp("D") == "0001100"
    assert comp("A") == "0110000"
    assert comp("M") == "1110000"
    assert comp("!D") == "0001101"
    assert comp("!A") == "0110001"
    assert comp("!M") == "1110001"
    assert comp("-D") == "0001111"
    assert comp("-A") == "0110011"
    assert comp("-M") == "1110011"
    assert comp("D+1") == "0011111"
    assert comp("A+1") == "0110111"
    assert comp("M+1") == "1110111"
    assert comp("D-1") == "0001110"
    assert comp("A-1") == "0110010"
    assert comp("M-1") == "1110010"
    assert comp("D+A") == "0000010"
    assert comp("D+M") == "1000010"
    assert comp("D-A") == "0010011"
    assert comp("D-M") == "1010011"
    assert comp("A-D") == "0000111"
    assert comp("M-D") == "1000111"
    assert comp("D&A") == "0000000"
    assert comp("D&M") == "1000000"
    assert comp("D|A") == "0010101"
    assert comp("D|M") == "1010101"

def test_jump() -> None:
    assert jump("") == "000"
    assert jump("JGT") == "001"
    assert jump("JEQ") == "010"
    assert jump("JGE") == "011"
    assert jump("JLT") == "100"
    assert jump("JNE") == "101"
    assert jump("JLE") == "110"
    assert jump("JMP") == "111"
